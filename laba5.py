import random

def z_1():
    # создаем список
    numbers = [random.randint(0, 20) for i in range(5)]

    # запрашиваем у пользователя число
    user_number = int(input("Введите число: "))

    # проверяем, есть ли число в списке
    if user_number in numbers:
        print("Поздравляю, Вы угадали число!")
    else:
        print("Нет такого числа!")

    # выводим исходный список и число пользователя
    print("Исходный список:", numbers)
    print("Число пользователя:", user_number)

def z_2():
    # создаем список любых элементов
    my_list = [1, 5, 7, 3, 2, 5]

    # создаем пустой словарь для подсчета элементов
    counter = {}

    # заполняем словарь
    for elem in my_list:
        counter[elem] = counter.get(elem, 0) + 1

    # ищем повторяющиеся элементы
    for key, value in counter.items():
        if value > 1:
            print("Элемент {} повторяется в списке".format(key))

def z_3():
    # задаем кортеж с названиями дней недели
    weekdays = ("Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота", "Воскресенье")

    # запрашиваем количество выходных дней
    num_weekends = int(input("Сколько дней отдыха вы хотите на неделе? "))

    # создаем списки выходных и рабочих дней
    weekend_days = weekdays[-num_weekends:]
    work_days = weekdays[:-num_weekends]

    # выводим списки на экран
    print("Ваши выходные дни: " + ", ".join(weekend_days))
    print("Ваши рабочие дни: " + ", ".join(work_days))


def z_4():
    # создаем два списка с фамилиями студентов
    group1 = ["Иванов", "Петров", "Сидоров", "Смирнов", "Кузнецов",
              "Соловьев", "Тихонова", "Козлова", "Василенко", "Громов"]
    group2 = ["Козлов", "Сергеев", "Лебедев", "Новиков", "Волков",
              "Матвеев", "Филиппова", "Денисова", "Жукова", "Комаров"]

    # объединяем списки в кортеж команды
    team = tuple(group1[:5] + group2[:5])

    # выводим исходные списки групп и кортеж команды
    print("Группа 1:", group1)
    print("Группа 2:", group2)
    print("Спортивная команда:", team)

    # выводим длину кортежа
    print("Длина кортежа команды:", len(team))

    # сортируем кортеж по алфавиту
    sorted_team = tuple(sorted(team))

    # выводим отсортированный кортеж
    print("Отсортированная команда:", sorted_team)

    # проверяем, входит ли "Иванов" в команду, и сколько раз он встречается
    count_of_ivanov = sorted_team.count("Иванов")
    if count_of_ivanov > 0:
        print("Фамилия Иванов встречается в команде")
        print("Количество: ", count_of_ivanov)
    else:
        print("Фамилия Иванов не входит в команду")


